# 设奇数和x
import math
n=99  # n可以改成奇数或偶数
sum=0
if(n%2==0):  # 当n为偶数，x+x+n/2=(n+1)*n/2
    sum=n**2/4
else:  # 当n为奇数，x+x-n+(n-1)/2=(n+1)*n/2
    sum=(n+1)**2/4
print(sum)

print('-'*40)

# S_n=1-3+5-7+...
# 法1
n=4  #  n是S下标
sign=1  # 符号或系数
sum=0
for i in range(1,2*n,2):
    sum+=sign*i
    sign=-sign
print(sum)
print('-'*40)

